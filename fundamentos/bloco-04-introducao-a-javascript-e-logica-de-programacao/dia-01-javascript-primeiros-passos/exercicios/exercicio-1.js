const a = 20;
const b = 15;

console.log("----- Adição -----");
console.log(`${a} + ${b} = ${a+b}`);


console.log("\n----- Subtração -----");
console.log(`${a} + ${b} = ${a-b}`);


console.log("\n----- Multiplacação -----");
console.log(`${a} * ${b} = ${a*b}`);


console.log("\n----- Divisão -----");
console.log(`${a} / ${b} = ${a/b}`);


console.log("\n----- Módulo -----");
console.log(`${a} % ${b} = ${a%b}`);
